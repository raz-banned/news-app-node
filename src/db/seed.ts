function getSeedImage(category: string, index: number): string {
  // Получаем тег по категории, либо берем дефолтный "news"
  const keyword = categoryKeywords[category] || "news";

  // lock=${index} гарантирует уникальность картинки для каждой строки в цикле
  return `https://loremflickr.com/800/450/${keyword}?lock=${index}`;
}

